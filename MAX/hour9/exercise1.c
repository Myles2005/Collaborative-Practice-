/*
Given the following statements,
int x;
unsigned int y;
x = 0xAB78;
y = 0xAB78;
write a program to display the decimal values of 
x and y on the screen.
*/
#include <stdio.h>

int main()
{
    int x;
    unsigned int y;
    x = 0xAB78;
    y = 0xAB78;
    printf("Decimal Value of x = %d\n", x);
    printf("Decimal Value of y = %d\n", y);

}
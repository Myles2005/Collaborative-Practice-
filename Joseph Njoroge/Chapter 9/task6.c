#include <stdio.h>
#include <math.h>

/**
5. Given an angle of 30 degrees, write a program to calculate its sine and tangent
values.
*/

int main(void)
{
    int y = 30;
    printf("The value of the tan of 30 is: %f\n", tan(y));
    printf("The value of the cos of 30 is: %f\n", cos(y));
    printf("The value of the sin of 30 is: %f\n", sin(y));
    return 0;
}
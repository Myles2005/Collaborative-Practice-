#include <stdio.h>

/**
*3. Write a program to multiply two signed int variables with positive values, and
display the result as a long int.
*/
 int main(void)
 {
    signed int x, y;
    x = 5;
    y = 6;

    printf("The value of addition of x(%d) and y(%d) is %ld\n", x, y, x + y);
    return 0;
 }